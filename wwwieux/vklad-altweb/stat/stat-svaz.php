<h2 id="5">O hře</h2>
<p>Předchozích sedm let znamenali Krvik Totr soubor rozličných, často se střídajících tváří. Nyní se představí v původní trojčlenné sestavě (Petr Jediný Novotný, Tomáš Kout &amp; Filip Votava). Bude to tedy vůbec poprvé, co tato komorní sestava odehraje divadlo &ndash; dříve totiž takto účinkovali spíše v zdramatizovaných autorských čteních. Poprvé v celé sedmnáctileté éře se také Krvik Totr obejdou bez písniček. Můžete se tedy těšit na čistě dramatické výkony&hellip;</p>
<p>Námět pro novou (v pořadí čtvrtou autorskou) hru <em>Svázaná</em> nalezli Krvik Totr v povídce francouzského autora Jeana Noixe <em>Kyselé hrozny</em>, původní text se však rozplynul v široké autorské dramaturgii Krvik Totr. Diskuse Novotného/Kouta nad novou látkou probíhaly v první polovině roku 2009, hra pak byla napsána za šest červencových dnů. Poprvé nenapsali hru na tělo konkrétním představitelům &ndash; jednotlivé role byly určeny až na první čtené zkoušce. Otevřená byla i otázka počtu postav &ndash; autorsky se Krvik Totr nevyhýbali příchodu dalších, ty ale nakonec vyklidily prostor základní skladební trojici. </p>
<p>Premiéra proběhla 2. a 5. listopadu 2009 v pražském Pidivadle.</p>
